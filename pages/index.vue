// Mint tokens with time-based restriction
async function mintTokens() {
  try {
    // Validate time-based restriction
    const lastMinted = await contract.methods.lastMinted().call();
    const currentTimestamp = Math.floor(Date.now() / 1000);
    const timeDiff = BigInt(currentTimestamp) - BigInt(lastMinted);
    if (timeDiff < 60n) {
      throw new Error("Please wait for 1 minute between mints");
    }

    // Retrieve token decimals from contract
    const tokenDecimals = await contract.methods.decimals().call();

    // Calculate mint value in Wei
    let mintValueInWei;
    if (isNaN(Number(mintVal.value))) {
      // Assume mintValue represents token amount
      mintValueInWei = BigInt(mintVal.value) * BigInt(10 ** tokenDecimals);
    } else {
      // Assume mintValue already represents Wei
      mintValueInWei = mintVal.value * (10 ** 18);
    }
    const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
    account.value = accounts[0];
    // Prepare the transaction
    const transaction = contract.methods.mint(account.value, mintValueInWei);

    // Estimate gas and convert to hex
    const estimatedGas = await transaction.estimateGas({ from: account.value });
    const gasPrice = web3.utils.toHex(web3.utils.toWei("0", "gwei"));

    const tx = {
      from: account.value,
      to: CONTRACT_ADDRESS,
      gasPrice,
      gas: estimatedGas.toString(),
      data: transaction.encodeABI(),
    };

    // Sign and send transaction with MetaMask
    const signedTx = await window.ethereum.request({ method: "eth_sendTransaction", params: [tx] });
    console.log("Transaction receipt:", signedTx);
  } catch (error) {
    console.error("Error while minting tokens:", error);
    // Implement specific error handling for user feedback
  }
}

// Connect to MetaMask and update balance
async function connectWallet() {
  try {
    const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
    account.value = accounts[0];
    balance.value = await web3.eth.getBalance(account.value);
    getBalance();
  } catch (error) {
    console.error("Error connecting to MetaMask:", error);
  }
}

// Fetch token balance
async function getBalance() {
  try {
    const rawBalance = BigInt(await contract.methods.balanceOf(account.value).call());
    const decimals = BigInt(await contract.methods.decimals().call());
    tokenBal.value = rawBalance / BigInt(10 ** Number(decimals));
  } catch (error) {
    console.error("Error fetching token balance:", error);
  }
}

